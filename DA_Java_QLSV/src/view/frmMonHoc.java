    private void btn_Search_DialogSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_Search_DialogSearchActionPerformed
        // TODO add your handling code here:
        try {
            Vector tieuDe = new Vector();
            tieuDe.add("Mã môn học");
            tieuDe.add("Môn học");
            Vector duLieu = new Vector();
            String TenMH = new String();
            TenMH= txt_TenMH_DialogSearch.getText();
            Label_KetQuaTimKiem.setText("Kết quả tìm kiếm tên môn học theo từ khóa: \""+TenMH+"\". Nhấn làm tươi để xem tất cả môn học");
            List<MonHoc> listMonHoc = controller.MonHocController.TKMonHoc(TenMH);
            if (listMonHoc != null) {
                for (MonHoc monHoc : listMonHoc) {
                    Vector tam = new Vector();
                    tam.add(monHoc.getID_MH());
                    tam.add(monHoc.getTenMH());
                    duLieu.add(tam);
                }
            }
            DefaultTableModel model = new DefaultTableModel(duLieu, tieuDe);
            tableMonHoc.setModel(model);
            Dialog_Search_MonHoc.dispose();
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(frmMonHoc.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btn_Search_DialogSearchActionPerformed

