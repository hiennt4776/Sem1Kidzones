    private void deleteMonHocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteMonHocActionPerformed
        // TODO add your handling code here:
        if (tableMonHoc.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Chưa chọn môn học cần xóa!", "Thông báo", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                String monHocID = tableMonHoc.getValueAt(tableMonHoc.getSelectedRow(), 0).toString();
                if (controller.MonHocController.deleteMonHocID(monHocID) == 1) {
                    JOptionPane.showMessageDialog(null, "Xóa thành công môn học có ID " + monHocID, "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                    Run_QLMonHoc();
                } else {
                    JOptionPane.showMessageDialog(null, "Xóa không thành công có môn học có ID " + monHocID, "Thông báo", JOptionPane.ERROR_MESSAGE);
                }
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(frmMonHoc.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_deleteMonHocActionPerformed

