    private void button_AddEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_AddEditActionPerformed
          reset();
        try {
            int selectedRow = jTable1.getSelectedRow();
            
            if (selectedRow == -1) {
                JOptionPane.showMessageDialog(null, "Chua chon sinh vien", "Thong bao", JOptionPane.ERROR_MESSAGE);
                return;
            }
           
             Label_IDSV.setText(jTable1.getValueAt(selectedRow, 0).toString());
              Label_TenSV.setText(jTable1.getValueAt(selectedRow, 1).toString());
 
              if(!(jTable1.getValueAt(selectedRow, 2).toString()==(" "))){
                    text_dialog_diemBT.setText(jTable1.getValueAt(selectedRow, 2).toString());
              }
              if(!(jTable1.getValueAt(selectedRow, 3).toString()==(" "))){
                    text_dialog_diemGK.setText(jTable1.getValueAt(selectedRow, 3).toString());
              }
              if(!(jTable1.getValueAt(selectedRow, 4).toString()==(" "))){
                    text_dialog_diemCK.setText(jTable1.getValueAt(selectedRow, 4).toString());
              }
              
            
            dialog_Edit.setSize(400,300);
            dialog_Edit.setLocationRelativeTo(null);
            dialog_Edit.setVisible(true);
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_button_AddEditActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      
         try {
        
        KetQua ketQua = new KetQua();
        
        if (text_dialog_diemBT.getText().isEmpty()) ketQua.setBaiTap(-1); else ketQua.setBaiTap(Float.parseFloat(text_dialog_diemBT.getText()));
        if (text_dialog_diemGK.getText().isEmpty())  ketQua.setGiuaKy(-1); else ketQua.setGiuaKy(Float.parseFloat(text_dialog_diemGK.getText()));          
        if (text_dialog_diemCK.getText().isEmpty()) ketQua.setCuoiKy(-1); else ketQua.setCuoiKy(Float.parseFloat(text_dialog_diemCK.getText()));
        
        if((ketQua.getGiuaKy()==-1)||(ketQua.getBaiTap()==-1)||(ketQua.getCuoiKy()==-1)){
            ketQua.setTrungBinh((float) -1.0);
            ketQua.setXepLoai(null);
        }
        else {
            ketQua.setTrungBinh((float) (0.2*ketQua.getBaiTap()+0.2*ketQua.getGiuaKy()+0.6*ketQua.getCuoiKy()));
            if (ketQua.getTrungBinh()>=8.5){
                ketQua.setXepLoai("A");
            }else if (ketQua.getTrungBinh()>=7 &&ketQua.getTrungBinh()<8.5){
                    ketQua.setXepLoai("B");
                }else if(ketQua.getTrungBinh()>=5.5 &&ketQua.getTrungBinh()<7){
                        ketQua.setXepLoai("C");
                }else if(ketQua.getTrungBinh()>=4 &&ketQua.getTrungBinh()<5.5){
                        ketQua.setXepLoai("D");    
                }else ketQua.setXepLoai("F");
        }
        
        ketQua.setID_LOPHP(Label_IDLHP.getText());
          
        ketQua.setID_SV(Label_IDSV.getText());
            int thongBao = controller.KetQuaController.editKetQua(ketQua);
            if(thongBao>0){
                 JOptionPane.showMessageDialog(null, "Cap nhat thanh cong", "Thong bao", JOptionPane.INFORMATION_MESSAGE);
                refresh();
                 dialog_Edit.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Cap nhat khong thanh cong", "Thong bao", JOptionPane.ERROR_MESSAGE);            
            }
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(frmKetQua.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(frmKetQua.class.getName()).log(Level.SEVERE, null, ex);
        }
            
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

