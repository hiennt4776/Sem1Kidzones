private void refresh(){
 try {
            String className = cmbLopHP.getSelectedItem().toString();
     //       System.out.println(className);         
      
            String ID_LopHP = new String();
            for (LopHP lopHP : listLopHP) {
                if (lopHP.getTenLopHP().equalsIgnoreCase(className)) {
                   
                        ID_LopHP = lopHP.getID_LopHP();
                        Label_IDLHP.setText(ID_LopHP.toString());
                        Label_LopHP.setText(className.toString()); 
                        break;
                   
                }
            }
            
            Vector tieuDe = new Vector();
            
            tieuDe.add("Mã Sinh Viên");
            tieuDe.add("Tên Sinh Viên");
            tieuDe.add("Bài Tập");
            tieuDe.add("Giữa Kỳ");
            tieuDe.add("Cuối Kỳ");
            tieuDe.add("Trung Bình");
            tieuDe.add("Xếp Loại");
            
            List<SinhVien> listSinhVien= controller.SinhVienController.listSinhVienID();
            Vector duLieu = new Vector();
            List<KetQua> listKetQua = controller.KetQuaController.getKetQuaByClassName(ID_LopHP);
            if(listKetQua!=null){
                for(KetQua ketQua: listKetQua){
                    Vector tam = new Vector();
                    tam.add(ketQua.getID_SV());
                    for (SinhVien sinhvien : listSinhVien){
                    if (sinhvien.getID_SV().equalsIgnoreCase(ketQua.getID_SV())) {
                        tam.add(sinhvien.getTenSV());
                        break;           
                    }    
                }
                    if (ketQua.getBaiTap()==-1) tam.add(" "); else tam.add(ketQua.getBaiTap());
                    if (ketQua.getGiuaKy()==-1) tam.add(" "); else tam.add(ketQua.getGiuaKy());
                    if (ketQua.getCuoiKy()==-1) tam.add(" "); else tam.add(ketQua.getCuoiKy());
                    if (ketQua.getTrungBinh()==-1) tam.add(" "); else tam.add(ketQua.getTrungBinh());
                     
//                    tam.add(ketQua.getTrungBinh());
                   tam.add(ketQua.getXepLoai());
                    duLieu.add(tam);

                }
               DefaultTableModel model = new DefaultTableModel(duLieu,tieuDe );
            jTable1.setModel(model); 
            }      
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
}
    
