    private void btn_SearchNV_DialogActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SearchNV_DialogActionPerformed
        // TODO add your handling code here:
       
            List<ChucVu> listChucVu;
        try {
            listChucVu = controller.ChucVuController.listChucVuID();
       
            Vector tieuDe = new Vector();
            tieuDe.add("Mã nhân viên");
            tieuDe.add("Tên nhân viên");
            tieuDe.add("Chức vụ");
            tieuDe.add("Giới tính");
            tieuDe.add("Số CMND");
            tieuDe.add("Ngày sinh");
            tieuDe.add("Địa chỉ");
            tieuDe.add("Lương");
            Vector duLieu = new Vector();
            String TenNV = new String();
            TenNV= txt_TenNV_Search.getText();
            Label_KetQuaTimKiem.setText("Kết quả tìm kiếm tên nhân viên theo từ khóa: "+TenNV+". Nhấn làm tươi để xem tất cả nhân viên");
            List<NhanVien> listNhanVien = controller.NhanVienController.TimKiemNVTen(TenNV);
            if (listNhanVien!= null) {
                  for (NhanVien nhanVien : listNhanVien) {
                    Vector tam = new Vector();
                    tam.add(nhanVien.getID_NV());
                    tam.add(nhanVien.getTenNV());
                   for (ChucVu chucVu : listChucVu) {
                        if (chucVu.getID_CV().equalsIgnoreCase(nhanVien.getChucVu())) {
                            tam.add(chucVu.getTenCV());
                            break;
                        }
                    }
                    if (nhanVien.isGioitinhNV()) {
                        tam.add("Nam");
                    } else {
                        tam.add("Nữ");
                    }
                    tam.add(nhanVien.getCMND());
                    tam.add(nhanVien.getNgaysinhNV());
                    tam.add(nhanVien.getDiachiNV());
                    tam.add(nhanVien.getLuong()); 
                    duLieu.add(tam);
                }
            }
            DefaultTableModel model = new DefaultTableModel(duLieu, tieuDe);
            tableNhanVien.setModel(model);
            Dialog_Search_NhanVien.dispose();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(frmNhanVien.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(frmNhanVien.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btn_SearchNV_DialogActionPerformed

