    private void btnAddNhanVien_DialogNhanVienActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddNhanVien_DialogNhanVienActionPerformed
        // TODO add your handling code here:
         try {
            // TODO add your handling code here:
            if (txtTenNV_DialogAddNhanVien.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Chưa nhập tên nhân viên", "Thông báo", JOptionPane.ERROR_MESSAGE);
                txtTenNV_DialogAddNhanVien.requestFocus();
                return;
            }
           
             if (txtDiachiNV_DialogAddNhanVien.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Chưa nhập địa chỉ nhân viên", "Thông báo", JOptionPane.ERROR_MESSAGE);
                txtDiachiNV_DialogAddNhanVien.requestFocus();
                return;
            }
             
             try{
                long n=0L;
                n = Long.parseLong(txtCMND_DialogAddNhanVien.getText());
                if(n<0) {
                    JOptionPane.showMessageDialog(null, "Chỉ nhập số ô số CMND ", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                return;
                }
            }catch(Exception ex) {
               JOptionPane.showMessageDialog(null, "Chỉ nhập số ô số CMND", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
             
            try{
                long n=0L;
                n = Long.parseLong(txtLuongNV_DialogAddNhanVien.getText());
                if(n<0) {
                    JOptionPane.showMessageDialog(null, "Chỉ nhập số ô Lương", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                return;
                }
            }catch(Exception ex) {
               JOptionPane.showMessageDialog(null, "Chỉ nhập số ô số Lương", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                return;
            } 
             
            
            NhanVien nhanVien = new NhanVien();
            nhanVien.setID_NV(txtNVID_DialogAddNhanVien.getText());
            nhanVien.setTenNV(txtTenNV_DialogAddNhanVien.getText());
            nhanVien.setCMND(txtCMND_DialogAddNhanVien.getText());
            for (ChucVu chucVu : listChucVu) {
                if (chucVu.getTenCV().equalsIgnoreCase(cmbChucVu_DialogAddNhanVien.getSelectedItem().toString())) {
                    nhanVien.setChucVu(chucVu.getID_CV());
                }
            }
            
            if (radioMale_DialogAddNhanVien.isSelected()) {
                nhanVien.setGioitinhNV(true);
            } else {
                nhanVien.setGioitinhNV(false);
            }

            nhanVien.setNgaysinhNV(jDateNgaysinhNV_DialogAddNhanVien.getDate());
            
            nhanVien.setDiachiNV(txtDiachiNV_DialogAddNhanVien.getText());

            nhanVien.setLuong(Integer.parseInt(txtLuongNV_DialogAddNhanVien.getText()));

            nhanVien.setPassword(jPasswordField1.getText().toString());
      //       System.out.println("Password: "+nhanVien.getPassword());
            
            int ketQua = controller.NhanVienController.addNhanVienID(nhanVien);
            if (ketQua > 0) {
                JOptionPane.showMessageDialog(null, "Thêm mới nhân viên thành công", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
                Run_QLNhanVien();
                Dialog_Add_NhanVien.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Thêm mới nhân viên không thành công", "Thông Báo", JOptionPane.ERROR_MESSAGE);
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(frmNhanVien.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NoSuchAlgorithmException ex) {
            Logger.getLogger(frmNhanVien.class.getName()).log(Level.SEVERE, null, ex);
        } catch (UnsupportedEncodingException ex) {
            Logger.getLogger(frmNhanVien.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAddNhanVien_DialogNhanVienActionPerformed

