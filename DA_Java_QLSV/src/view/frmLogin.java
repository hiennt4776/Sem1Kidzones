    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(this.txtIDNV.getText().length()==0 || String.valueOf(this.jPasswordNV.getText()).length()==0)
        {
            JOptionPane.showMessageDialog(this,"Bạn chưa nhập thông tin tài khoản hoặc mật khẩu","Thông báo",2);
        }
        else
        {
            try {

                Connection connection = controller.connectDB.connectSQLServer();
                PreparedStatement command = connection.prepareStatement("Select * from NHANVIEN where ID_NV=? and password = ?");
                command.setString(1, txtIDNV.getText());
                command.setString(2, Encrypt_MD5.encrypt(jPasswordNV.getText()));
                ResultSet rs = command.executeQuery();

                if (rs.next()) {
                    IDNV = txtIDNV.getText();
                    frmMain frmmain = new frmMain();
                    JOptionPane.showMessageDialog(null, "Đăng nhập thành công");
                    dispose();
                    //                System.out.println(IDNV);

                    if(IDCV(IDNV).equals("CVID01")){
                        setVisible(false);
                        frmmain.setVisible(true);
                        frmmain.setVisibleAdmin();
                    }else
                    if(IDCV(IDNV).equals("CVID02")){
                        setVisible(false);
                        frmmain.setVisible(true);
                        frmmain.setVisibleQuanLy();
                    }else
                    if(IDCV(IDNV).equals("CVID03")){
                        setVisible(false);
                        frmmain.setVisible(true);
                        frmmain.setVisibleGiangVien();
                    }else
                    if(IDCV(IDNV).equals("CVID04")){
                        setVisible(false);
                        frmmain.setVisible(true);
                        frmmain.setVisibleThuNgan();
                    }

                } else {
                    JOptionPane.showMessageDialog(null, "Sai ID nhân viên hoặc password!");
                }

            } catch (ClassNotFoundException ex) {
                Logger.getLogger(frmLogin.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(frmLogin.class.getName()).log(Level.SEVERE, null, ex);
            } catch (NoSuchAlgorithmException ex) {
                Logger.getLogger(frmLogin.class.getName()).log(Level.SEVERE, null, ex);
            } catch (UnsupportedEncodingException ex) {
                Logger.getLogger(frmLogin.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

 
   
    
