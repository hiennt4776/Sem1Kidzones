    private void editChucVuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editChucVuActionPerformed
        // TODO add your handling code here:
        int selectedRow = tableChucVu.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Chưa chọn chức vụ cần sửa", "Thông báo", JOptionPane.ERROR_MESSAGE);
            txtChucVu_DialogEditChucVu.requestFocus();
            return;
        }
 
        txtChucVuID_DialogEditChucVu.setText(tableChucVu.getValueAt(selectedRow, 0).toString());
        txtChucVu_DialogEditChucVu.setText(tableChucVu.getValueAt(selectedRow, 1).toString());
        Dialog_Edit_ChucVu.setSize(300, 160);
        Dialog_Edit_ChucVu.setLocationRelativeTo(null);
        Dialog_Edit_ChucVu.setVisible(true);
    }//GEN-LAST:event_editChucVuActionPerformed

    private void deleteChucVuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteChucVuActionPerformed
        // TODO add your handling code here:
        if (tableChucVu.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Chưa chọn chức vụ cần xóa!", "Thông báo", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                String chucVuID = tableChucVu.getValueAt(tableChucVu.getSelectedRow(), 0).toString();
                if (controller.ChucVuController.deleteChucVuID(chucVuID) == 1) {
                    JOptionPane.showMessageDialog(null, "Xoa thành công chức vụ có ID " + chucVuID, "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                    Run_QLChucVu();
                } else {
                    JOptionPane.showMessageDialog(null, "Xoa không thành công chức vụ có ID " + chucVuID, "Thông báo", JOptionPane.ERROR_MESSAGE);
                }
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(frmChucVu.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_deleteChucVuActionPerformed

