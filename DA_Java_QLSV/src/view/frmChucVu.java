private void Run_QLChucVu(){
     try {
            Vector tieuDe = new Vector();
            tieuDe.add("Mã chức vụ");
            tieuDe.add("Chức vụ");
            Vector duLieu = new Vector();
            List<ChucVu> listChucVu = controller.ChucVuController.listChucVuID();
            if (listChucVu != null){
            for (ChucVu chucVu : listChucVu) {
                Vector tam = new Vector();
                tam.add(chucVu.getID_CV());
                tam.add(chucVu.getTenCV());
                duLieu.add(tam);
            }
            }
            DefaultTableModel model = new DefaultTableModel(duLieu, tieuDe);
            tableChucVu.setModel(model);
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(frmChucVu.class.getName()).log(Level.SEVERE, null, ex);
        }
}
