    private void btnAddSinhVien_DialogSinhVienActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddSinhVien_DialogSinhVienActionPerformed
        // TODO add your handling code here:
         try {
            // TODO add your handling code here:
            if (txtTenSV_DialogAddSinhVien.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Chưa nhập tên sinh viên", "Thông báo", JOptionPane.ERROR_MESSAGE);
                txtTenSV_DialogAddSinhVien.requestFocus();
                return;
            }
            
             if (txtDiachiSV_DialogAddSinhVien.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Chưa nhập địa chỉ sinh viên", "Thông báo", JOptionPane.ERROR_MESSAGE);
                txtDiachiSV_DialogAddSinhVien.requestFocus();
                return;
            }
            
                if (txtCMND_DialogAddSinhVien.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Chua nhập số CMND sinh viên", "Thông bao", JOptionPane.ERROR_MESSAGE);
                txtCMND_DialogAddSinhVien.requestFocus();
                return;
            }
            
             try{
                long n=0L;
                n = Long.parseLong(txtCMND_DialogAddSinhVien.getText());
                if(n<0) {
                    JOptionPane.showMessageDialog(null, "Chỉ nhập số trên ô số CMND", "Thong bao", JOptionPane.ERROR_MESSAGE);
                return;
                }
            }catch(Exception ex) {
               JOptionPane.showMessageDialog(null, "Chỉ nhập số trên ô số CMND", "Thong bao", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            SinhVien sinhVien = new SinhVien();
            sinhVien.setID_SV(txtSVID_DialogAddSinhVien.getText());
            sinhVien.setTenSV(txtTenSV_DialogAddSinhVien.getText());
            for (LopSH lopSH : listLopSH) {
                if (lopSH.getTenLopSH().equalsIgnoreCase(cmbLopSH_DialogAddSinhVien.getSelectedItem().toString())) {
                    sinhVien.setLopSH(lopSH.getID_LopSH());
                }
            }
            
            if (radioMale_DialogAddSinhVien.isSelected()) {
                sinhVien.setGioitinhSV(true);
            } else {
                sinhVien.setGioitinhSV(false);
            }
            
            sinhVien.setNgaysinhSV(jDateNgaysinhSV_DialogAddSinhVien.getDate());
             
            sinhVien.setDiachiSV(txtDiachiSV_DialogAddSinhVien.getText());
            sinhVien.setCMND(txtCMND_DialogAddSinhVien.getText());
            int ketQua = controller.SinhVienController.addSinhVienID(sinhVien);
            if (ketQua > 0) {
                JOptionPane.showMessageDialog(null, "Thêm mới sinh viên thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                Run_QLSinhVien();
                Dialog_Add_SinhVien.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Thêm mới sinh viên không thành công", "Thong bao", JOptionPane.ERROR_MESSAGE);
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(frmSinhVien.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAddSinhVien_DialogSinhVienActionPerformed

