    private void btn_SearchSV_DialogActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SearchSV_DialogActionPerformed
        // TODO add your handling code here:
        try {
            List<LopSH> listLopSH = controller.LopSHController.listLopSHID();
            Vector tieuDe = new Vector();
            tieuDe.add("Mã sinh viên");
            tieuDe.add("Tên sinh viên");
            tieuDe.add("Lớp SH");
            tieuDe.add("Giới tính");
            tieuDe.add("Số CMND");
            tieuDe.add("Ngày sinh");
            tieuDe.add("Địa chỉ");
            Vector duLieu = new Vector();
            String TenSV = new String();
            TenSV= txt_TenSV_Search.getText();
            Label_KetQuaTimKiem.setText("Kết quả tìm kiếm tên sinh viên theo từ khóa: "+TenSV+". Nhấn làm tươi để xem tất cả sinh viên");
            List<SinhVien> listSinhVien = controller.SinhVienController.TimKiemSVTen(TenSV);
            if (listSinhVien != null) {
                for (SinhVien sinhVien : listSinhVien) {
                    Vector tam = new Vector();
                    tam.add(sinhVien.getID_SV());
                    tam.add(sinhVien.getTenSV());
                    for (LopSH lopSH : listLopSH) {
                        if (lopSH.getID_LopSH().equalsIgnoreCase(sinhVien.getLopSH())) {
                            tam.add(lopSH.getTenLopSH());
                            break;
                        }
                    }
                    if (sinhVien.isGioitinhSV()) {
                        tam.add("Nam");
                    } else {
                        tam.add("Nữ");
                    }
                    tam.add(sinhVien.getCMND());
                    
                    SimpleDateFormat formatter = new SimpleDateFormat("dd/MM/yyyy");
                    String strDate = formatter.format(sinhVien.getNgaysinhSV());
                    tam.add(strDate);

                    tam.add(sinhVien.getDiachiSV());
                    duLieu.add(tam);
                }
            }
            DefaultTableModel model = new DefaultTableModel(duLieu, tieuDe);
            tableSinhVien.setModel(model);
            Dialog_Search_SinhVien.dispose();
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(frmSinhVien.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btn_SearchSV_DialogActionPerformed

